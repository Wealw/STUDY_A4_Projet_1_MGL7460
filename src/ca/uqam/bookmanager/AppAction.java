package ca.uqam.bookmanager;

/**
 * Define all possible user action in main app menu.
 */
public enum AppAction {
    /**
     * Represent user choice to quit the application.
     */
    QUIT,
    /**
     * Represent user choice to get more info un users.
     */
    USER,
    /**
     * Represent user choice to get more info on books.
     */
    BOOK,
}
